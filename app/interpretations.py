# 解釈文を取得するヘルパー関数 (オプション)
def get_interpretation_text(category, key1, key2=None, key3=None):
    """指定されたキーに基づいて解釈文を取得する"""
    if category == "planet_in_sign":
        return PLANET_IN_SIGN_INTERPRETATIONS.get(key1, {}).get(key2, "解釈が見つかりません。")
    elif category == "planet_in_house":
        # ハウス番号は整数なので int() でキャスト
        try:
            house_num = int(key2)
            return PLANET_IN_HOUSE_INTERPRETATIONS.get(key1, {}).get(house_num, "解釈が見つかりません。")
        except (ValueError, TypeError):
             return "ハウス番号が無効です。"
    elif category == "aspect":
        # アスペクトのキー生成ロジック (例: Sun_Jupiter_Trine)
        # key1=planet1, key2=planet2, key3=aspect_type と想定
        if key1 and key2 and key3:
             # 天体名の順番を揃える (例: Moon_Sun ではなく Sun_Moon にする)
             planets = sorted([key1, key2])
             aspect_key = f"{planets[0]}_{planets[1]}_{key3}"
             return ASPECT_INTERPRETATIONS.get(aspect_key, "解釈が見つかりません。")
        else:
            return "アスペクト情報が不十分です。"
    else:
        return "不明なカテゴリです。" 